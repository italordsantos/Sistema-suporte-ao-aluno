    private void jlbEditarAtendenteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlbEditarAtendenteMouseClicked
        // TODO add your handling code here:
        boolean a = false;
        DefaultTableModel tabela = (DefaultTableModel) jtDadosAtendente.getModel();
        for (int i = 0; i < jtDadosAtendente.getRowCount(); i++) {
            if (jtDadosAtendente.isRowSelected(i)) {
                a = true;
            }
        }

        if (jtDadosAtendente.getRowCount() < 1) {
            JOptionPane.showMessageDialog(this, "FaÃ§a uma pesquisa para selecionar o atendente que deseja editar");

        } else {
            for (int i = 0; i < jtDadosAtendente.getRowCount(); i++) {
                if (jtDadosAtendente.isRowSelected(i)) {
                    a = true;
                }
            }

            if (a) {
                int linha = jtDadosAtendente.getSelectedRow(), colunaNucleo = 2, colunaSiape = 1, tipo = 1;
                String siape = jtDadosAtendente.getValueAt(linha, colunaSiape).toString(),
                        nucleo = jtDadosAtendente.getValueAt(linha, colunaNucleo).toString();
                if (nucleo.equals("ADMIN")) {
                    JOptionPane.showMessageDialog(null, "NÃ£o Ã© possÃ­vel editar um usuÃ¡rio com acesso ADM");
                } else {
                    c.abrirTelaCadastrarEditarAtendente(tipo, siape, tabela);
                    c.limparTabela(tabela);
                    
                }

            } else {
                JOptionPane.showMessageDialog(this, "Favor selecionar um atendente antes de editar");
            }
        }
    }//GEN-LAST:event_jlbEditarAtendenteMouseClicked

