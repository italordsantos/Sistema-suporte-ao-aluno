public class MResponsavel {
    String sNome;
    String sCelular;
    String sTelefone;
    String sParentesco;
    String sMatriculaDoFilho;

    public String getsNome() {
        return sNome;
    }

    public void setsNome(String sNome) {
        this.sNome = sNome;
    }

    public String getsCelular() {
        return sCelular;
    }

    public void setsCelular(String sCelular) {
        this.sCelular = sCelular;
    }

    public String getsTelefone() {
        return sTelefone;
    }

    public void setsTelefone(String sTelefone) {
        this.sTelefone = sTelefone;
    }

    public String getsParentesco() {
        return sParentesco;
    }

    public void setsParentesco(String sParentesco) {
        this.sParentesco = sParentesco;
    }

    public String getsMatriculaDoFilho() {
        return sMatriculaDoFilho;
    }

    public void setsMatriculaDoFilho(String sMatriculaDoFilho) {
        this.sMatriculaDoFilho = sMatriculaDoFilho;
    }
    
    
}
